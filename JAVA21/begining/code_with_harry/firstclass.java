public class firstclass {
    class employee{
        int salary;
        String name;
        String company;
    }
    public static void main(String[] args) {
        employee sai = new employee();
        sai.salary = 10;
        sai.name = "sai";
        sai.company = "aws";
        System.out.println(sai.salary);
    }
}
